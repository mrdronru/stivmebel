function h1(text) {
  return new Paragraph({ heading: HeadingLevel.HEADING_1, spacing: { before: 360, after: 160 }, children: [new TextRun({ text, bold: true, color: ACCENT })] });
}
function h2(text) {
  return new Paragraph({ heading: HeadingLevel.HEADING_2, spacing: { before: 280, after: 120 }, children: [new TextRun({ text, bold: true, color: ACCENT })] });
}
function p(runs, opts = {}) {
  if (typeof runs === 'string') runs = [new TextRun({ text: runs })];
  return new Paragraph({ spacing: { after: 120, line: 276 }, ...opts, children: runs });
}
function bullet(runs) {
  if (typeof runs === 'string') runs = [new TextRun({ text: runs })];
  return new Paragraph({ numbering: { reference: "bullets", level: 0 }, spacing: { after: 80, line: 276 }, children: runs });
}
function numbered(runs, ref) {
  if (typeof runs === 'string') runs = [new TextRun({ text: runs })];
  return new Paragraph({ numbering: { reference: ref, level: 0 }, spacing: { after: 80, line: 276 }, children: runs });
}
function b(text) { return new TextRun({ text, bold: true }); }
function t(text) { return new TextRun({ text }); }
function it(text) { return new TextRun({ text, italics: true }); }

// Шаблон сообщения: абзац с фоном и левой рамкой
function template(text) {
  return new Paragraph({
    spacing: { before: 60, after: 160, line: 276 },
    indent: { left: 360, right: 360 },
    shading: { type: ShadingType.CLEAR, fill: QUOTE_BG },
    border: { left: { style: BorderStyle.SINGLE, size: 18, color: ACCENT, space: 8 } },
    children: [new TextRun({ text, italics: true })]
  });
}

// Таблица-хелпер
function makeTable(headers, rows, colWidthsMm) {
  const colWidths = colWidthsMm.map(convertMillimetersToTwip);
  const total = colWidths.reduce((a, x) => a + x, 0);
  const borders = {
    top: { style: BorderStyle.SINGLE, size: 4, color: "BBBBBB" },
    bottom: { style: BorderStyle.SINGLE, size: 4, color: "BBBBBB" },
    left: { style: BorderStyle.SINGLE, size: 4, color: "BBBBBB" },
    right: { style: BorderStyle.SINGLE, size: 4, color: "BBBBBB" },
    insideHorizontal: { style: BorderStyle.SINGLE, size: 4, color: "BBBBBB" },
    insideVertical: { style: BorderStyle.SINGLE, size: 4, color: "BBBBBB" },
  };
  const headerRow = new TableRow({
    tableHeader: true,
    children: headers.map((htext, i) => new TableCell({
      width: { size: colWidths[i], type: WidthType.DXA },
      shading: { type: ShadingType.CLEAR, fill: ACCENT },
      margins: { top: 80, bottom: 80, left: 120, right: 120 },
      children: [new Paragraph({ children: [new TextRun({ text: htext, bold: true, color: "FFFFFF", size: 20 })] })]
    }))
  });
  const bodyRows = rows.map((row, ri) => new TableRow({
    children: row.map((cell, i) => new TableCell({
      width: { size: colWidths[i], type: WidthType.DXA },
      shading: ri % 2 === 1 ? { type: ShadingType.CLEAR, fill: LIGHT } : undefined,
      margins: { top: 60, bottom: 60, left: 120, right: 120 },
      children: [new Paragraph({ spacing: { line: 260 }, children: [new TextRun({ text: cell, size: 20 })] })]
    }))
  }));
  return new Table({ width: { size: total, type: WidthType.DXA }, columnWidths: colWidths, borders, rows: [headerRow, ...bodyRows] });
}

